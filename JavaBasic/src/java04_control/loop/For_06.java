package java04_control.loop;

public class For_06 {

	public static void main(String[] args) {
		
		// Quiz.
		// 구구단 2단 출력하기
		int num = 2;
		int result;
		
		for(int i=1; i<10; i++) {
			result = num * i;
			System.out.println(num + " x " + i + " = " + result);
		}
		
		
		
		
	}

}
